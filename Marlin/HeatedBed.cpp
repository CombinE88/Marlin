bool HeatedBed__Present(void) {
	bool returnValue = false;
	if (READ(BED_AVAIL_PIN) == HIGH) {
		returnValue = true;
	}
	else {
		SERIAL_PROTOCOL("Heated Bed Removed!");
		SERIAL_EOL;
	}
	return returnValue;
}

